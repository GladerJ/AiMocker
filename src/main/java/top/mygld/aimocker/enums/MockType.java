package top.mygld.aimocker.enums;

/**
 * Defines how AiMock should generate mock data.
 */
public enum MockType {
    /**
     * Generate a single mock object.
     */
    SINGLE,

    /**
     * Generate a collection of mock objects.
     */
    COLLECTION,

    /**
     * Generate an array of mock objects.
     */
    ARRAY
}
